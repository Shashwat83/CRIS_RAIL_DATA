def train_bnn_and_get_g(X_tr, time_tr, event_tr, X_te, lr=1e-3, epochs=100):
    model = build_bnn(X_tr.shape[1])
    model.compile(
        optimizer=keras.optimizers.RMSprop(lr),
        loss=neg_log_partial_lik
    )

    y_tr = np.column_stack([event_tr, time_tr]).astype(np.float32)

    es = keras.callbacks.EarlyStopping(
        patience=10,
        restore_best_weights=True
    )

    timer = EpochTimer()

    model.fit(
        X_tr.astype(np.float32),
        y_tr,
        batch_size=X_tr.shape[0],   # full batch is important here
        epochs=epochs,
        verbose=0,
        validation_split=0.2,
        shuffle=False,
        callbacks=[es, timer]
    )

    g_tr = model.predict(X_tr.astype(np.float32), verbose=0).reshape(-1)
    g_te = model.predict(X_te.astype(np.float32), verbose=0).reshape(-1)

    return g_tr, g_te


