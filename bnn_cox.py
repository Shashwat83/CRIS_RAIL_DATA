# ================= Faster Cox Loss =================

@tf.function
def neg_log_partial_lik(y_true, y_pred):

    event = y_true[:, 0]
    risk = tf.squeeze(y_pred, axis=-1)

    # cumulative log-sum-exp
    log_cumsum = tf.math.log(
        tf.cumsum(tf.exp(risk), reverse=True) + 1e-12
    )

    loglik = tf.reduce_sum(event * (risk - log_cumsum))

    return -loglik / (tf.reduce_sum(event) + 1e-8)


