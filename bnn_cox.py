def prepare_survival_data(df):

    df = df.copy()

    # ---- parse dates
    start = pd.to_datetime(df[start_col], errors="coerce", dayfirst=True)
    fracture = pd.to_datetime(df[event_col], errors="coerce", dayfirst=True)

    # ---- event indicator
    df["event"] = fracture.notna().astype(int)

# ---- duration for fractured rows
    duration_event = (fracture - start).dt.days

# ---- administrative censoring time (dataset end)
    admin_censor_date = fracture.max()

# ---- duration for censored rows
    duration_censor = (admin_censor_date - start).dt.days

# ---- assign duration
    df["DURATION"] = duration_event
    df.loc[df["event"] == 0, "DURATION"] = duration_censor[df["event"] == 0]

    # ---- remove invalid rows
    mask = (
        start.notna()
        & df["DURATION"].notna()
        & (df["DURATION"] >= 0)
    )

    df = df.loc[mask].copy()

    # ---- SECTION frequency encoding
    freq_map = df[section_col].value_counts(normalize=True).to_dict()

    df["SECTION_FREQ"] = (
        df[section_col]
        .map(freq_map)
        .fillna(0)
    )

    # ---- feature matrix
    feature_cols = ohe_cols + num_cols + ["SECTION_FREQ"]

    X_input = df[feature_cols].copy()

    # ---- encoders
    if sklearn.__version__ >= "1.2":
        ohe_tf = OneHotEncoder(handle_unknown="ignore", sparse_output=False)
    else:
        ohe_tf = OneHotEncoder(handle_unknown="ignore", sparse=False)

    num_tf = StandardScaler()

    feature_ct = ColumnTransformer(
        transformers=[
            ("ohe", ohe_tf, ohe_cols),
            ("num", num_tf, num_cols),
            ("sec", "passthrough", ["SECTION_FREQ"])
        ],
        remainder="drop"
    )

    X = feature_ct.fit_transform(X_input)

    feature_names = feature_ct.get_feature_names_out()

    X_df = pd.DataFrame(X, columns=feature_names)

    # ---- targets
    T = df["DURATION"].to_numpy(dtype=float)
    E = df["event"].to_numpy(dtype=int)

    # ---- final checks
    mask = (
        np.isfinite(T)
        & np.isfinite(E)
        & np.all(np.isfinite(X), axis=1)
    )

    X_df = X_df.loc[mask]
    X = X[mask]
    T = T[mask]
    E = E[mask]

    # ---- breaks
    breaks = np.quantile(T, np.linspace(0, 1, 11))
    breaks[0] = 0.0
    breaks = np.unique(breaks)

    if breaks[-1] < np.max(T):
        breaks = np.append(breaks, np.max(T))

    return X_df, X, T, E, breaks

