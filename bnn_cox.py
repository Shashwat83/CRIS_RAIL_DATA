class AddEventFlag(BaseEstimator, TransformerMixin):
    """event = 1 if end date present, else 0."""
    def __init__(self, fracture_col=end_col):
        self.fracture_col = fracture_col
    def fit(self, X, y=None): return self
    def transform(self, X):
        X_ = X.copy()
        X_["event"] = np.where(pd.to_datetime(X_[self.fracture_col], errors="coerce").notna(), 1, 0)
        return X_

class DurationCalculator(BaseEstimator, TransformerMixin):
    """DURATION = (end - start).days; drops the two date cols."""
    def __init__(self, start_col=start_col, end_col=end_col, dayfirst=True):
        self.start_col, self.end_col, self.dayfirst = start_col, end_col, dayfirst
    def fit(self, X, y=None): return self
    def transform(self, X):
        X_ = X.copy()
        start = pd.to_datetime(X_[self.start_col], dayfirst=self.dayfirst, errors="coerce")
        end   = pd.to_datetime(X_[self.end_col],   dayfirst=self.dayfirst, errors="coerce")
        X_["DURATION"] = (end - start).dt.days
        X_.drop(columns=[self.start_col, self.end_col], inplace=True)
        return X_

class KFoldTargetEncoder(BaseEstimator, TransformerMixin):
    """OOF mean encoding for a single categorical column (SECTION)."""
    def __init__(self, col=section_col, n_splits=5, noise=0.0, random_state=42):
        self.col, self.n_splits, self.noise, self.random_state = col, n_splits, noise, random_state
    def fit(self, X, y):
        s = X[self.col].astype("category")
        self.global_mean_ = float(np.nanmean(y))
        self.cat_mean_ = pd.DataFrame({self.col: s, "_y": y}).groupby(self.col)["_y"].mean()
        return self
    def transform(self, X):
        s = X[self.col].astype("category")
        enc = s.map(self.cat_mean_).astype("float64").fillna(self.global_mean_)
        if self.noise > 0:
            enc = enc + np.random.RandomState(self.random_state).normal(0, self.noise, size=len(enc))
        return enc.to_numpy().reshape(-1, 1)
    def fit_transform(self, X, y):
        s = X[self.col].astype("category")
        kf = KFold(n_splits=self.n_splits, shuffle=True, random_state=self.random_state)
        oof = pd.Series(index=X.index, dtype="float64")
        y_arr = np.asarray(y)
        for tr, va in kf.split(X):
            means = pd.DataFrame({self.col: s.iloc[tr], "_y": y_arr[tr]}).groupby(self.col)["_y"].mean()
            oof.iloc[va] = s.iloc[va].map(means).astype("float64").fillna(float(np.nanmean(y_arr[tr])))
        self.fit(X, y)  # set full-data stats for inference
        if self.noise > 0:
            oof = oof + np.random.RandomState(self.random_state).normal(0, self.noise, size=len(oof))
        return oof.to_numpy().reshape(-1, 1)

class AddSectionEncoded(BaseEstimator, TransformerMixin):
    """Adds SECTION_ENC numeric column using target encoder."""
    def __init__(self, encoder: KFoldTargetEncoder):
        self.encoder = encoder
    def fit(self, X, y):
        self.encoder.fit(X, y)  # uses target y for mean encoding
        return self
    def transform(self, X):
        X_ = X.copy()
        X_["SECTION_ENC"] = self.encoder.transform(X)
        return X_

